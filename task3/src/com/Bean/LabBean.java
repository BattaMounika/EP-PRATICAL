package com.Bean;

public class LabBean {
	private int Itemid;
    private String Name;
    private int Cost;
	public int getItemid() {
		return Itemid;
	}
	public void setItemid(int itemid) {
		Itemid = itemid;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getCost() {
		return Cost;
	}
	public void setCost(int cost) {
		Cost = cost;
	}
	
    
}
